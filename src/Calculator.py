import math

def addition(a, b):
    c = a + b
    return c

def subtraction(a, b):
    c = a - b
    return c

def multiplication(a, b):
    c = a * b
    return c

def division(a, b):
    c = a/b
    return c

def square(a, b):
    c = a ** b
    return c

def squareroot(a):
    sqrt_a = math.sqrt(a)
    return(sqrt_a)
